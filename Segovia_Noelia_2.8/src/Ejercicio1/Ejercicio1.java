package Ejercicio1;

public class Ejercicio1 {

	public static void main(String[] args) {
		/*
		 * Este programa va a mostrar los números del 1 al 100 utilizando la instrucción while
		 * 
		 * Autora: Noelia Segovia Sánchez
		 * 
		 * Fecha 12/10/2022
		 * 
		 */

		int i = 1;

		while (i <= 100) {
			System.out.println(i);
			i = i + 1;
		}
	}
}
