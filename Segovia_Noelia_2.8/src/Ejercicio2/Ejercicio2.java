package Ejercicio2;

public class Ejercicio2 {

	public static void main(String[] args) {
		/*
		 * Este programa va a mostrar los números del 1 al 100 utilizando la instrucción
		 * do...while
		 * 
		 * Autora: Noelia Segovia Sánchez
		 * 
		 * Fecha 12/10/2022
		 * 
		 */

		int i = 1;

		do {
			System.out.println(i);
			i = i + 1;
		} while (i <= 100);
	}

}
