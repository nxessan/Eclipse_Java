package Ejercicio3;

public class Ejercicio3 {

	public static void main(String[] args) {
		/*
		 * Este programa va a mostrar los números del 100 al 1 utilizando la instrucción
		 * do...while
		 * 
		 * Autora: Noelia Segovia Sánchez
		 * 
		 * Fecha 12/10/2022
		 * 
		 */

		int i = 100;

		do {
			System.out.println(i);
			i = i - 1;
		} while (i >= 1);
	}

}
